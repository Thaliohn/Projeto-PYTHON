# Ler 2 números e imprimir a soma, subtração, multiplicação, divisão e exponenciação dos números

num1 = int(input("Digite um número: "))
num2 = int(input("Digite um outro número: "))
soma = num1 + num2
subt = num1 - num2
mult = num1 * num2
divi = float(num1 / num2)
expo = num1 ** num2
print(f'A soma é : {soma}, A subtração é: {subt}, A multiplicação é: {mult}, A divisão é: {divi}, A exponenciação é: {expo}.')